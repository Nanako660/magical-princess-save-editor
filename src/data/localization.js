/**
 * 获取本地化文本
 * @param {string} key - 翻译键
 * @param {string} fallback - 未找到时的回退文本
 * @returns {string}
 */
export function getLang(key, fallback = '') {
  return LangData[key] || fallback || key
}

/**
 * 获取本地化文本（带参数替换）
 * @param {string} key - 翻译键
 * @param {object} params - 替换参数 {s1: value1, s2: value2}
 * @param {string} fallback - 未找到时的回退文本
 * @returns {string}
 */
export function getLangWithParams(key, params = {}, fallback = '') {
  let text = getLang(key, fallback)
  for (const [k, v] of Object.entries(params)) {
    text = text.replace(`%${k}`, v)
  }
  return text
}
